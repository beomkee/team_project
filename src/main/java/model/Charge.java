package model;

public class Charge {

	private String c_num;
	private String c_size;
	private String c_area;
	private String c_charge;
	
	public String getC_num() {
		return c_num;
	}
	public void setC_num(String c_num) {
		this.c_num = c_num;
	}
	public String getC_size() {
		return c_size;
	}
	public void setC_size(String c_size) {
		this.c_size = c_size;
	}
	public String getC_area() {
		return c_area;
	}
	public void setC_area(String c_area) {
		this.c_area = c_area;
	}
	public String getC_charge() {
		return c_charge;
	}
	public void setC_charge(String c_charge) {
		this.c_charge = c_charge;
	}
}
